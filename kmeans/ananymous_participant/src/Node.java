
public class Node {
	private int port;
	private String name;
	private String type;
	private int nodeNo;
	
	private int sensingLoss;
	private int fusionLoss;
	String hash;
	
	
	
	public String getHash() {
		return hash;
	}
	public void setHash(String hash) {
		this.hash = hash;
	}
	public int getPort() {
		return port;
	}
	public void setPort(int port) {
		this.port = port;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public int getSensingLoss() {
		return sensingLoss;
	}
	public void setSensingLoss(int sensingLoss) {
		this.sensingLoss = sensingLoss;
	}
	public int getFusionLoss() {
		return fusionLoss;
	}
	public void setFusionLoss(int fusionLoss) {
		this.fusionLoss = fusionLoss;
	}
	public int getNodeNo() {
		return nodeNo;
	}
	public void setNodeNo(int nodeNo) {
		this.nodeNo = nodeNo;
	}
	
	
	
	
	
	
}
